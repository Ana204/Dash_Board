//Metodo para filtra meus dados que vem dos sensores e me retornar os dados que eu preciso de cada paciênte que eu quiser
//Modo de uso :
//  1 Argumento = o Nome do Paciente em que queremos pegar os dados
//  2 Argumento = o numero do tipo de dados que eu quero que retorne
//
//        1 - para retornar temperatura do quarto
//        2 - para retornar temperatura do paciente
//        3 - para retornar frequencia cardiaca
//        4 - para retornar o nivel do oxigenio
//        5 - para retornar a pressao arterial
//        6 - para retornar a data

function criarDadosDoPaciente (nome, dado) 
{
    let paciente1 = []
    let temperaturaDoQuarto1 = []
    let TemperaturaDoPaciênte1 = []
    let frequenciaCardiaca1 = []
    let nivelDeOxigenio1 = []
    let pressaoArteria1 = []
    let data1 = []

    let infASerMandado = null

    switch (dado) {
        case 1:
            infASerMandado = temperaturaDoQuarto1
            break;
        case 2:
            infASerMandado = TemperaturaDoPaciênte1
            break;    
        case 3:
            infASerMandado = frequenciaCardiaca1
            break;            
        case 4:
            infASerMandado = nivelDeOxigenio1
            break;   
        case 5:
            infASerMandado = pressaoArteria1
            break;  
        case 6:
            infASerMandado = data1
            break;          

        default:
            return undefined
            break;
    }


    console.log(nome)

    meusDados.forEach( element => {
        if (element.nome == nome) 
        paciente1.push(element) 

    });



    paciente1.forEach(element => {
        temperaturaDoQuarto1.push(element.Temperatura_do_quarto)   
        
        
    });

    paciente1.forEach(element => {
        TemperaturaDoPaciênte1.push(element.Temperatura_do_paciênte)   
        
        
    });

    paciente1.forEach(element => {
        frequenciaCardiaca1.push(element.Frequência_cardíaca)   
        
        
    });

    paciente1.forEach(element => {
        nivelDeOxigenio1.push(element.Nível_de_oxigênio)   
        
        
    });

    paciente1.forEach(element => {
        pressaoArteria1.push(element.Pressão_arterial)   
        
        
    });

    paciente1.forEach(element => {
        data1.push(element.Data)   
        
        
    });

    return infASerMandado

    


}









